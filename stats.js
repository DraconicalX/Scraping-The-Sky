class Stats{
  constructor(health,maxHealth,healthRegen,defense,speed,damage,mana,manaCap,manaRegen){
    this.HP=health
    this.HPC=maxHealth
    this.HPR = healthRegen
    this.DEF=defense
    this.SPD = speed
    this.DMG = damage
    this.MNA = mana
    this.MNAC = manaCap
    this.MNAR = manaRegen
  }
}